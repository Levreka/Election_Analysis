x = 0
# creating condition_controlled loop aka while loop
# condition is testing if X is less than or equal to 5
while x <= 5:
    # if condition is true print the value of x it will continue printing
    # until condition is not longer true
    print(x)
    # this code increments x by 1 and continusly loop until x is not 
    # longer greater than or equal to 5 and stops
    x = x + 1